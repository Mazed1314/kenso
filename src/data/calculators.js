/** Filter calculators by section: 'live' = main gallery, 'lab' = Lab page */
export const getByStatus = (status) =>
  CALCULATORS.filter((c) => c.status === status);

/** Get all live calculators (excluding lab-only) */
export const getLiveCalculators = () => getByStatus("live");

/** Get all lab-status calculators */
export const getLabCalculators = () => getByStatus("lab");

/** Group live calculators by category */
export const getByCategory = () => {
  const grouped = {};
  getLiveCalculators().forEach((calc) => {
    if (!grouped[calc.category]) grouped[calc.category] = [];
    grouped[calc.category].push(calc);
  });
  return grouped;
};

/** Search calculators by keyword */
export const searchCalculators = (query) => {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return CALCULATORS.filter(
    (c) =>
      c.title.toLowerCase().includes(q) ||
      c.desc.toLowerCase().includes(q) ||
      c.tags.some((t) => t.toLowerCase().includes(q)),
  );
};

/** Get a calculator by id */
export const getById = (id) => CALCULATORS.find((c) => c.id === id);

/** Count of live calculators */
export const TOTAL_LIVE = getLiveCalculators().length;
